TermCtrl *TeNew(uint32_t* pFramebufferAddress, uint32_t FramebufferWidth, uint32_t FramebufferHeight, uint32_t FramebufferPitch, uint32_t Background, uint32_t Foreground, uint32_t CursorColor) {
    TermCtrl *pTerm = MmPoolAlloc(sizeof(TermCtrl));
    pTerm->CursorX = 0;
    pTerm->CursorY = 0;
    pTerm->CursorWidth = 8;
    pTerm->CursorHeight = 16;
    pTerm->CursorColor = CursorColor;

    pTerm->Foreground = Foreground;
    pTerm->Background = Background;

    pTerm->FontWidth = 8;
    pTerm->FontHeight = 16;
    pTerm->pFontData = g_aDefaultFont;

    pTerm->pFramebufferAddress = pFramebufferAddress;
    pTerm->FramebufferWidth = FramebufferWidth;
    pTerm->FramebufferHeight = FramebufferHeight;
    pTerm->FramebufferPitch = FramebufferPitch;

    for (uint32_t i = 0; i < pTerm->FramebufferWidth * pTerm->FramebufferHeight; i++) {
        pTerm->pFramebufferAddress[i] = pTerm->Background;
    }

    TeDrawFillRect(pTerm, pTerm->CursorX, pTerm->CursorY, pTerm->CursorWidth, pTerm->CursorHeight, pTerm->CursorColor);

    return pTerm;
}

static inline bool BitAddressFromMask(uint32_t Convert, int Return) {
    int Mask = 1 << (Return - 1);
    return ((Convert & Mask) != 0);
}

void TeDrawFillRect(TermCtrl *pTermCtrl, uint32_t x, uint32_t y, uint32_t Width, uint32_t Height, uint32_t color) {
    for (uint32_t y1 = 0; y1 < Height; y1++) {
        for (uint32_t x1 = 0; x1 < Width; x1++) {
            pTermCtrl->pFramebufferAddress[(y1 + y) * pTermCtrl->FramebufferPitch / 4 + (x1 + x)] = color;
        }
    }
}

bool TeCheckScroll(TermCtrl *pTermCtrl) {
    if (pTermCtrl->CursorY >= pTermCtrl->FramebufferHeight) {
        // Do scrolling.
        for (int y = pTermCtrl->FontHeight; y != pTermCtrl->FramebufferHeight; y++) {
            for (int x = 0; x < pTermCtrl->FramebufferWidth; x++) {
                uint32_t Src = y * pTermCtrl->FramebufferPitch / 4 + x;
                uint32_t Dst = (y - pTermCtrl->FontHeight) * pTermCtrl->FramebufferPitch / 4 + x;
                pTermCtrl->pFramebufferAddress[Dst] = pTermCtrl->pFramebufferAddress[Src];
            }
        }
        for (int y = pTermCtrl->FramebufferHeight - pTermCtrl->FontHeight; y != pTermCtrl->FramebufferHeight; y++) {
            for (int x = 0; x != pTermCtrl->FramebufferWidth; x++){
                pTermCtrl->pFramebufferAddress[y * pTermCtrl->FramebufferPitch / 4 + x] = pTermCtrl->Background;
            }
        }

        pTermCtrl->CursorX = 0;
        pTermCtrl->CursorY = pTermCtrl->FramebufferHeight - pTermCtrl->CursorHeight;
        return true;
    }
    return false;
}

void TeWriteChar(TermCtrl *pTermCtrl, char Char) {
    if (Char == 0) return;
    TeDrawFillRect(pTermCtrl, pTermCtrl->CursorX, pTermCtrl->CursorY, pTermCtrl->CursorWidth, pTermCtrl->CursorHeight, pTermCtrl->Background);
    if (Char == '\n') {
        pTermCtrl->CursorX = 0;
        pTermCtrl->CursorY += pTermCtrl->FontHeight;
        TeCheckScroll(pTermCtrl);
        TeDrawFillRect(pTermCtrl, pTermCtrl->CursorX, pTermCtrl->CursorY, pTermCtrl->CursorWidth, pTermCtrl->CursorHeight, pTermCtrl->CursorColor);
        return;
    }
    uint32_t Point = pTermCtrl->FontHeight * Char;
    for (size_t y = 0; y < pTermCtrl->FontHeight; y++) {
        for (size_t x = 0; x < pTermCtrl->FontWidth; x++) {
            if (BitAddressFromMask(pTermCtrl->pFontData[Point + y], x + 1))
                pTermCtrl->pFramebufferAddress[(pTermCtrl->CursorY + y) * pTermCtrl->FramebufferPitch / 4 + (pTermCtrl->CursorX + (pTermCtrl->FontWidth - x))] = pTermCtrl->Foreground;
        }
    }
    pTermCtrl->CursorX += pTermCtrl->FontWidth;
    if (pTermCtrl->CursorX > pTermCtrl->FramebufferWidth) {
        if (!TeCheckScroll(pTermCtrl)) {
            pTermCtrl->CursorX = 0;
            pTermCtrl->CursorY += pTermCtrl->FontHeight;
        }
    }
    TeDrawFillRect(pTermCtrl, pTermCtrl->CursorX, pTermCtrl->CursorY, pTermCtrl->CursorWidth, pTermCtrl->CursorHeight, pTermCtrl->CursorColor);
}
